function estimateDurationMs(text: string): number {
  // Very rough: ~15 chars/sec of speech, with a 500ms floor.
  const ms = Math.max(500, Math.round((text.length / 15) * 1000));
  return ms;
}

export const mockTTS: TTSProvider = {
  async synthesize(request: TTSRequest): Promise<TTSResponse> {
    return {
      provider: "mock",
      bytes: SILENT_MP3_FRAME,
      contentType: "audio/mpeg",
      durationMs: estimateDurationMs(request.text),
      estimatedCostUsd: 0,
      voiceId: request.voiceId,
    };
  },
};
