// Mock TTS provider. Returns a tiny silent MP3 (the smallest valid MP3 frame
// repeated a few times) and a faked duration so the audio pipeline can be
// exercised end-to-end without hitting a real TTS API.
//
// The bytes below are a valid MP3 frame of silence at 44.1kHz mono 32kbps.
// Players will play this as ~0.1s of silence; we report a fake duration based
// on the text length so downstream scene timing feels right.

